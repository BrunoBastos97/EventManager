package model;

public class CafeModel {
	
	private int id;
	private String nome;
	private int lotacao;
	
	public int getId() {
		return this.id;
	}
	
	public void setId(int id) {
		this.setId(id);
	}
	
	public String getNome() {
		return this.nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getLotacao(int lotacao) {
		return this.lotacao;
	}
	
	public void setLotacao(int lotacao) {
		this.lotacao = lotacao;
	}

}
