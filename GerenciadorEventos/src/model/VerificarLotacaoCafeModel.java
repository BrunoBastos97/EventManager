package model;

public class VerificarLotacaoCafeModel {
	
	private int lotacao;
	private int countPessoa;
	private int lotacaoGeral;
	private int ultimoIdCafe;
	
	public int getUltimoIdCafe() {
		return ultimoIdCafe;
	}
	public void setUltimoIdCafe(int ultimoIdCafe) {
		this.ultimoIdCafe = ultimoIdCafe;
	}
	public int getLotacao() {
		return lotacao;
	}
	public void setLotacao(int id) {
		this.lotacao = id;
	}
	public int getCountPessoa() {
		return countPessoa;
	}
	public void setCountPessoa(int countPessoa) {
		this.countPessoa = countPessoa;
	}
	public int getLotacaoGeral() {
		return lotacaoGeral;
	}
	public void setLotacaoGeral(int lotacaoGeral) {
		this.lotacaoGeral = lotacaoGeral;
	}
	
	
}
